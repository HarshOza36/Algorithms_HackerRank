def equalizeArray(arr):
    # Write your code here
    freq = {}
    for ele in arr:
        if(ele in freq):
            freq[ele] += 1
        else:
            freq[ele] = 1
    
    max_value = max(freq.values())
    return len(arr) - max_value
    

