def quickSort(arr):
    # Write your code here
    pivot = arr[0]
    l, r = [], []
    for i in arr:
        if(i == pivot):
            continue
        elif(i < pivot):
            l.append(i)
        elif(i > pivot):
            r.append(i)
    return l + [pivot] + r

